# Make a function lowercase that, given a string, returns the string, except in all lowercase letters.
#>>>>lowercase('ARG')
#arg
#>>>>lowercase('TRINKET')
#trinket

def lowercase(string):
	# Add code here that returns the answer
	return string.lower()

# Add print statements here to see what your code does:
print(lowercase('ARGS'))
print(lowercase('TRINKET'))

#>>>>uppercase('arg')
# ARG
#>>>>uppercase('trinket')
# TRINKET

def uppercase(string):
	return string.upper()

print(uppercase('arg'))
print(uppercase('trinket'))
